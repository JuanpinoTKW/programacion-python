'''
Realizá un programa que permita ingresar dos número. 
Debes mostrar los resultados para las 4 operaciones matemáticas básicas con los números ingresados
'''
numero_1 = int(input("ingrese un numero entero: "))
numero_2 = int(input("ingrese otro numero entero: "))
suma = numero_1 + numero_2
resta = numero_1 - numero_2
multipl = numero_2 * numero_1
division = numero_1 / numero_2
print(f"la suma de los dos números es = {suma}, la resta es = {resta}, la multiplicacion es {multipl}, y la division es {division}.")